package firstweek.dayone;

/*
 * Greater Than or Equal To:
Write a program to check if a number is greater than 
or equal to another number using the >= operator.

Less Than or Equal To:
Write a program to check if a number is less than or
 equal to another number using the <= operator.
 */
public class greaterThanOrLessThan {
    public static void main(String[] args) {
        int n1 = 5254;
        int n2 = 5154;

        boolean num = n1 >= n2;
        System.out.println(num);

        boolean num1 = n1 <= n2;
        System.out.println(num1);

    }
}
