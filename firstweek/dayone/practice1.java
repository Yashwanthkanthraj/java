package firstweek.dayone;

/*Write a program to add two numbers and print the result.

Write a program to subtract two numbers and print the result.

Write a program to multiply two numbers and print the result.

Write a program to divide two numbers and print the result.

Write a program to find the remainder when one number is divided
 by another using the modulo operator %.*/

public class practice1 {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 36;

        int add = num1 + num2;
        int sub = num2 - num1;
        int mul = num1 * num2;
        int div = num2 / num1;
        int modulo = num2 % num1;

        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(modulo);

    }

}
